package febuary.task;

public class task1
{
    public static void main(String[] args) {
        int num1=10,num2=20;
    int result=num1+num2;

     switch(result)
     {
         case '+':
             System.out.println("num1+num2");
             break;
         case '-':
             System.out.println("num1-num2");
             break;
         case '*':
             System.out.println("num1*num2");
             break;
         case '/':
             System.out.println("num1/num2");
             break;
         case '%':
             System.out.println("num1%num2");
             break;
         default:
             System.out.println("invalid");


     }




    }
}
