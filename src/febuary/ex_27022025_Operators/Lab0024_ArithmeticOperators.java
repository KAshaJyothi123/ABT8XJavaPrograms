package febuary.ex_27022025_Operators;

public class Lab0024_ArithmeticOperators
{
    public static void main(String[] args) {
        //+.-,*,%,/
        int a=10;
        int b=30;
        System.out.println(a+b);
        System.out.println(a-b);
        System.out.println(a*b);
        System.out.println(a/b);

        // %modules Reaminder
        System.out.println(a%b);

      /*  |2|10|5| --quotient
          | 10 |
          |0   |  ---Reaminder  */
    }
}
